def SpecialCharacter(level):
	#密码最常见搭配特殊字符,默认使用
	lis = ['!','@','#','$','%','.','&','~']

	#全部特殊字符
	lis2 = ['~','!','@','#','$','%','^','&','*','(',')','-','+','=','_','`','\\','|','/','<','>',':',';','"','\'','[',']','{','}','.',',','?']

	if level == 0:
		return lis
	elif level == 1:
		return lis2